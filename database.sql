create table users (
  id int not null,
  username varchar(100) not null,
  password varchar(100) not null,
  email varchar(100) not null,
  name varchar(100) not null,
  phone varchar(15) not null,
  birthday date not null,
  address varchar(100) not null,
  primary key (id)
);
create table posts (
  id int not
